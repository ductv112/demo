// ─── Helper: get latest classification for a product ─────

export const getLatestClassification = (productId: string): ProductClassificationRecord | undefined => {
  const records = productClassifications.filter(r => r.productId === productId);
  if (!records.length) return undefined;
  // Sort by createdAt desc, prefer applied > pending > draft
  const priority: Record<string, number> = { applied: 0, pending_approval: 1, returned_for_edit: 2, approved: 3, draft: 4, rejected: 5, not_classified: 6 };
  return records.sort((a, b) => {
    const pa = priority[a.classificationStatus] ?? 9;
    const pb = priority[b.classificationStatus] ?? 9;
    if (pa !== pb) return pa - pb;
    return new Date(b.createdAt).getTime() - new Date(a.createdAt).getTime();
  })[0];
};

// ─── Summary stats ────────────────────────────────────────

export const getClassificationStats = () => {
  // For list view: count unique products by their "active" classification status
  const productMap = new Map<string, ProductClassificationRecord>();
  for (const r of productClassifications) {
    const existing = productMap.get(r.productId);
    const priority: Record<string, number> = { applied: 0, pending_approval: 1, returned_for_edit: 2, approved: 3, draft: 4, rejected: 5 };
    if (!existing || (priority[r.classificationStatus] ?? 9) < (priority[existing.classificationStatus] ?? 9)) {
      productMap.set(r.productId, r);
    }
  }
  const records = Array.from(productMap.values());
  return {
    total: records.length,
    applied: records.filter(r => r.classificationStatus === 'applied').length,
    pending: records.filter(r => r.classificationStatus === 'pending_approval').length,
    draft: records.filter(r => r.classificationStatus === 'draft').length,
    rejected: records.filter(r => r.classificationStatus === 'rejected').length,
    returned: records.filter(r => r.classificationStatus === 'returned_for_edit').length,
  };
};
